 Q 2: WHICH COUNTRIES HAVE MOST MOST INVOICES?
 
SELECT COUNT(*) AS C, BILLING_COUNTRY
FROM INVOICE GROUP BY BILLING_COUNTRY
ORDER BY C DESC

 Q 3: WHAT ARE THE TOP 3 VALUES OF TOTAL INVOICE?
 
 SELECT * FROM INVOICE 
 ORDER BY TOTAL DESC
 LIMIT 3
 
