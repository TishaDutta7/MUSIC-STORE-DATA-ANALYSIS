 WE MADE THE MOST MONEY . WRITE A QUERY THAT RETURNS ONE CITY THAT HAS THE HIGHEST SUM OF INVOICE TOTALS.
 RETURN BOTH THE CITY NAME & SUM OF ALL INVOICE TOTALS.
 
